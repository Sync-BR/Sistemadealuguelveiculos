    public  static void  DataAtual(){
         Date dataAtual = new Date();
         Locale brasil = new  Locale("pt","BR");
        SimpleDateFormat dateFormat = new SimpleDateFormat("y-MM-dd", brasil);
        String dataFormatada = dateFormat.format(dataAtual);
        AluguelVeiculos.txtDataAluguel.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.DateFormatter(java.text.DateFormat.getDateInstance(java.text.DateFormat.SHORT, brasil))));
        AluguelVeiculos.txtDataAluguel.setText(dataFormatada);
        
        
        //Calcular Calendario
        
        Calendar calendario = Calendar.getInstance();
        calendario.setTime(dataAtual);
        calendario.add(Calendar.MONTH,1);
        Date dataEntrega = calendario.getTime();
        String DataEntregar = dateFormat.format(dataEntrega);
        AluguelVeiculos.txtDataEntregar.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.DateFormatter(java.text.DateFormat.getDateInstance(java.text.DateFormat.SHORT))));
        
        AluguelVeiculos.txtDataEntregar.setText(DataEntregar);
    }
    

