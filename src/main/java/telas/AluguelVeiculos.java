    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Verificar se os campos estão vazio
        
        if (VeiculoLista == null || txtDataAluguel == null || txtDataEntregar == null || txtCliente == null || txtTotal == null || txtObservacao == null) {
            AluguelVeiculos.txtStatus.setText("Prenchar todos os campos");
        } else {
            //Pegar todas informações do campo e adicionar em uma variavel
            int id = 1;
            String observacao = txtObservacao.getText();
            String VeiculoClientes = VeiculoLista.getSelectedItem().toString();
            String AluguelCliente = txtCliente.getSelectedItem().toString();
            //Verificar status da entregar
            if (EntregueSim.isSelected()) {
                StatusVeiculo = true;
            } else {
                StatusVeiculo = false;
            }
            String DataClienteAluguel = txtDataAluguel.getText();
            String DataClienteEntregar = txtDataEntregar.getText();
            BigDecimal ValorReals = new BigDecimal(txtTotal.getText());
            String ValorNews = ValorReals.toString();
            
            //Alugar veiculo
            try {
                String DateStr = txtDataAluguel.getText();
                AluguelBeans ReservarVeiculo = new AluguelBeans();
                ReservarVeiculo.setDataAluguel(ReservarVeiculo.getDataAluguel());
                ReservarVeiculo.setDataEntrega(ReservarVeiculo.getDataEntrega());
                ReservarVeiculo.setCliente(ReservarVeiculo.getCliente());
                ReservarVeiculo.setObservacao(ReservarVeiculo.getObservacao());
                ReservarVeiculo.setValorPago(ReservarVeiculo.getValorPago());
                AluguelDAO alugueldeveiculos = new AluguelDAO();
                alugueldeveiculos.inserirVeiculo(ReservarVeiculo);
            } catch (Exception e) {
                
                JOptionPane.showMessageDialog(null, e + "", "Erro ao Alugar um veículo", JOptionPane.INFORMATION_MESSAGE);

            }

            //Verificar se foi inserido o aluguel
            if(AluguelDAO.Aluguelinserido == true){
               txtStatus.setText(" Aluguel inserido com sucesso!");
               txtCliente.setSelectedItem(0);
               VeiculoLista.setSelectedItem(0);
               txtTotal.setText("");
               txtObservacao.setText("");
            } else{
               txtStatus.setText(" Falha ao inserir o aluguel.");
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

